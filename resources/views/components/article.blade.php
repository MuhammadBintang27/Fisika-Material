<script>
document.addEventListener('DOMContentLoaded', function() {
    const articleElements = document.querySelectorAll('.article-animate');
    const articleSection = document.getElementById('articles');
    let lastScrollY = window.scrollY;
    let articleSectionTop = articleSection.offsetTop - 200;

    const observer = new IntersectionObserver((entries) => {
        entries.forEach(entry => {
            const currentScrollY = window.scrollY;
            const scrollDirection = currentScrollY > lastScrollY ? 'down' : 'up';

            if (entry.isIntersecting) {
                const delay = entry.target.dataset.delay || 0;
                setTimeout(() => {
                    entry.target.classList.add('animate');
                }, delay);
            } else {
                if (scrollDirection === 'up' && currentScrollY < articleSectionTop) {
                    entry.target.classList.remove('animate');
                }
            }

            lastScrollY = currentScrollY;
        });
    }, {
        threshold: 0.1,
        rootMargin: '0px 0px -100px 0px'
    });

    articleElements.forEach(element => {
        observer.observe(element);
    });

    window.addEventListener('resize', () => {
        articleSectionTop = articleSection.offsetTop - 200;
    });
});
</script>       